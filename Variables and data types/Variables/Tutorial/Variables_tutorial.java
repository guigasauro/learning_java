class Variables_tutorial{
	public static void main(String[] args){

		// These are the main types of variables in Java

		// Integer
		int x = 123;

		// Double
		double y = 3.14;

		// Boolean
		boolean z = true;

		// Char
		char character = '!';

		// String
		String name = "Guilherme";

		System.out.println("This is an integer: "+ x);
		System.out.println("This is a double: "+ y);
		System.out.println("This is a boolean value: "+ z);
		System.out.println("This is a char: "+ character);
		System.out.println("This is a String: "+ name);

	}
}