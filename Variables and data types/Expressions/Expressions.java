
// Showcasing some of the basic numeric expressions in Java
class Expressions{
	public static void main(String[] args){
		int num = 10;

		// Subtraction
		num = num - 5;

		System.out.println("10 - 5 = "+ num);

		// Addition
		num = num + 15;

		System.out.println("5 + 15 = "+ num);

		// Multiplication
		num = num * 2;

		System.out.println("20 * 2 = "+ num);

		// Division
		num = num / 4;

		System.out.println("40 / 4 = "+ num);

		// Remainder of a division
		num = num % 2;

		System.out.println("The remainder of 10 / 2 is: "+ num);

		// Incrementing
		num++;

		System.out.println("0++ = "+ num);

		// Decrementing
		num--;

		System.out.println("1-- = "+ num);
	}
}